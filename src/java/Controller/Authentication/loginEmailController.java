package Controller.Authentication;

import DAO.AccountDAO;
import Model.Account;
import Model.AccountGoogle;
import Model.Constants;
import com.google.gson.Gson;
import com.google.gson.JsonObject;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.apache.http.client.ClientProtocolException;
import org.apache.http.client.fluent.Form;
import org.apache.http.client.fluent.Request;

/**
 *
 * @author dell
 */
public class loginEmailController extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //lay code xac thuc google tra ve
        String code = request.getParameter("code");
        
        //lay thong tin nguoi dung bang code gg tra ve
        String accessToken = getToken(code);
        AccountGoogle accGoogle = getUserInfo(accessToken);
        AccountDAO aDao = new AccountDAO();
        Account acc = aDao.getAccountByGgId(accGoogle.getId());
        //kiem tra xem tai khoan gg da ton tai chua
        if (acc != null) {
            request.getSession().setAttribute("account", acc);
            response.sendRedirect("home");
        } else {
            request.getRequestDispatcher("registerMainPage.jsp").forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

    public static String getToken(String code) throws ClientProtocolException, IOException {
        // goi api de lay token
        String response = Request.Post(Constants.GOOGLE_LINK_GET_TOKEN)
                .bodyForm(Form.form().add("client_id", Constants.GOOGLE_CLIENT_ID)
                        .add("client_secret", Constants.GOOGLE_CLIENT_SECRET)
                        .add("redirect_uri", Constants.GOOGLE_REDIRECT_URI).add("code", code)
                        .add("grant_type", Constants.GOOGLE_GRANT_TYPE).build())
                .execute().returnContent().asString();
        
        //chuyen doi ma token thanh chuoi json
        JsonObject jobj = new Gson().fromJson(response, JsonObject.class);
        String accessToken = jobj.get("access_token").toString().replaceAll("\"", "");
        return accessToken;
    }

    public static AccountGoogle getUserInfo(final String accessToken) throws ClientProtocolException, IOException {
        
        String link = Constants.GOOGLE_LINK_GET_USER_INFO + accessToken;
        //lay du lieu nguoi dung tu token tra ve
        String response = Request.Get(link).execute().returnContent().asString();
        //map json tra ve voi AccountGoogle
        AccountGoogle googlePojo = new Gson().fromJson(response, AccountGoogle.class);

        return googlePojo;
    }
}
